#!/usr/bin/env python3
"""
gen_results_js.py — results.json → src/results_data.js 변환기
SmartSwing-NH v12.0

사용법:
  python scripts/gen_results_js.py
  (backtest_engine.py 실행 후 호출)

출력: src/results_data.js
  export const RESULTS_DATA = { tradeLog, curve, kpi, validationTrace, params, generated }
"""

import json
import pathlib
import datetime

RESULTS_JSON = pathlib.Path(__file__).parent / "results.json"
OUTPUT_JS    = pathlib.Path(__file__).parent.parent / "src" / "results_data.js"


def main():
    if not RESULTS_JSON.exists():
        print(f"❌  {RESULTS_JSON} 없음 — backtest_engine.py 먼저 실행하세요")
        return

    with open(RESULTS_JSON, encoding="utf-8") as f:
        data = json.load(f)

    # 필드 검증
    required = ["tradeLog", "curve", "kpi", "params", "validationTrace"]
    missing  = [k for k in required if k not in data]
    if missing:
        print(f"❌  results.json에 필수 필드 없음: {missing}")
        return

    kpi = data["kpi"]
    print(f"✓ results.json 읽기 완료")
    print(f"  기간: {kpi.get('start')} ~ {kpi.get('end')}")
    print(f"  누적수익: +{kpi.get('totalRet')}%  MDD: {kpi.get('mdd')}%  Sharpe: {kpi.get('sharpe')}")
    print(f"  거래수: {kpi.get('trades')}건  승률: {kpi.get('winRate')}%")
    print(f"  validationTrace: {len(data['validationTrace'])}개월")

    # JS 문자열 생성
    json_str = json.dumps(data, ensure_ascii=False, separators=(",", ":"))

    now_str = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
    js_content = f"""// AUTO-GENERATED by gen_results_js.py — {now_str}
// DO NOT EDIT MANUALLY — backtest_engine.py 실행 후 재생성
// SmartSwing-NH v12.0 | Python 일별 엔진

export const RESULTS_DATA = {json_str};

export const {{ tradeLog, curve, kpi, validationTrace, params, generated }} = RESULTS_DATA;
"""

    OUTPUT_JS.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_JS, "w", encoding="utf-8") as f:
        f.write(js_content)

    size_kb = OUTPUT_JS.stat().st_size / 1024
    print(f"✓ {OUTPUT_JS} 생성 완료 ({size_kb:.1f} KB)")


if __name__ == "__main__":
    main()
